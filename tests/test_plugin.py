"""
Tests for snapfix.plugin — the pytest plugin.

These tests verify plugin behaviour using pytest's own testing utilities.
"""
import os
import pathlib
import sys

import pytest


# ── Import surface ────────────────────────────────────────────────────────────

def test_plugin_imports():
    from snapfix.plugin import (
        pytest_addoption,
        pytest_configure,
        pytest_sessionstart,
        pytest_collection_modifyitems,
        pytest_collect_file,
        SnapfixFixtureFile,
        snapfix_store,
        snapfix_config,
    )
    assert callable(pytest_addoption)
    assert callable(pytest_configure)
    assert callable(pytest_collect_file)


def test_snapfix_fixture_file_is_pytest_module():
    from snapfix.plugin import SnapfixFixtureFile
    assert issubclass(SnapfixFixtureFile, pytest.Module)


